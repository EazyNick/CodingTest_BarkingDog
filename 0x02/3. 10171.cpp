/*
고양이 출력
cout << "문자" << "\n"
인데, 

문자에 \ 들어가면, \\로 인식시켜줘야함!
*/

#include <bits/stdc++.h>

using namespace std;

int main(void){
    ios::sync_with_stdio(0);
    cin.tie(0);

    cout << "\\    /\\" << "\n";
    cout << " )  ( ')" << "\n";
    cout << "(  /  )" << "\n";
    cout << " \\(__)|" << "\n";
}